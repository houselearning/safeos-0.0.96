#include "keyboard.h"
#include "io.h"

void keyboard_init(void) {
    // Enable keyboard interrupt
    outb(0x21, inb(0x21) & ~0x02); // Clear bit 1 (IRQ1)
}

uint8_t keyboard_read_scancode(void) {
    return inb(0x60);
}

