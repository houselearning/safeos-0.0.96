# Macro for ISRs without error code
.macro ISR_NOERRCODE num
_isr\num:
    cli
    push $0
    push $\num
    jmp isr_common_stub
.endm

# Macro for ISRs with error code
.macro ISR_ERRCODE num
_isr\num:
    cli
    push $\num
    jmp isr_common_stub
.endm

# Macro for IRQs
.macro IRQ num, irqnum
_irq\num:
    cli
    push $0
    push $\irqnum
    jmp irq_common_stub
.endm

